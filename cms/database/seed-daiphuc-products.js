async function seedDaiphucProducts(db) {
  console.log('🌱 Seeding 119 Nhôm Kính products from Dai Phuc...');
  
  let count = 0;
  for (const item of productsData) {
    const p = item.product;
    
    // Check if product already exists by slug
    const existing = db.prepare("SELECT id FROM content WHERE slug = ?").get(p.slug);
    if (existing) {
      // Already seeded
      continue;
    }
    
    const timestamp = new Date().toISOString();
    const dbTime = new Date().toISOString().replace('T', ' ').substring(0, 19);
    
    // 1. Insert content
    const res = db.prepare(`
      INSERT INTO content (
        type, title, slug, status, author_id, thumbnail, excerpt, category, published_at, created_at, updated_at
      ) VALUES ('product', ?, ?, 'published', 1, ?, ?, ?, ?, ?, ?)
    `).run(p.title, p.slug, p.thumbnail, p.excerpt, p.category, timestamp, dbTime, dbTime);
    
    const contentId = res.lastInsertRowid;
    
    // 2. Insert meta
    const insertMeta = db.prepare(`
      INSERT INTO content_meta (content_id, field_key, field_value, field_type)
      VALUES (?, ?, ?, 'text')
    `);
    for (const [key, val] of Object.entries(item.meta)) {
      insertMeta.run(contentId, key, val);
    }
    
    // 3. Insert blocks
    const insertBlock = db.prepare(`
      INSERT INTO blocks (content_id, type, position, data, visible, created_at, updated_at)
      VALUES (?, ?, ?, ?, ?, ?, ?)
    `);
    for (const b of item.blocks) {
      insertBlock.run(contentId, b.type, b.position, JSON.stringify(b.data), b.visible, dbTime, dbTime);
    }
    
    count++;
  }
  
  console.log(`✅ Seeded ${count} new Nhôm Kính products successfully.`);
}

module.exports = { seedDaiphucProducts };
